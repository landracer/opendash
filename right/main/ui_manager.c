/* ======================================================================
 * HELPER: get display suffix strings for the current page
 * ====================================================================== */

static const char* get_primary_suffix(uint8_t page)
{
    if (page >= GAUGE_PAGE_COUNT) return "";
    const gauge_page_t *p = &s_gauge_pages[page];
    if (p->is_primary_temp)     return opendash_get_temp_suffix(current_layout.temp_unit);
    if (p->is_primary_pressure) return opendash_get_pressure_suffix(current_layout.pressure_unit);
    if (p->is_primary_speed)    return opendash_get_speed_suffix(current_layout.speed_unit);
    /* RPM, ratio, etc. — no unit suffix or use the DP's own */
    if (p->primary_dp == OPENDASH_DP_RPM) return "rpm";
    return "";
}

static const char* get_secondary_suffix(uint8_t page)
{
    if (page >= GAUGE_PAGE_COUNT) return "";
    const gauge_page_t *p = &s_gauge_pages[page];
    if (p->is_secondary_temp)     return opendash_get_temp_suffix(current_layout.temp_unit);
    if (p->is_secondary_pressure) return opendash_get_pressure_suffix(current_layout.pressure_unit);
    if (p->is_secondary_speed)    return opendash_get_speed_suffix(current_layout.speed_unit);
    if (p->secondary_dp == OPENDASH_DP_AFR) return ":1";
    return "";
}

static float convert_primary(uint8_t page, float raw)
{
    if (page >= GAUGE_PAGE_COUNT) return raw;
    const gauge_page_t *p = &s_gauge_pages[page];
    if (p->is_primary_temp)     return opendash_convert_temp(raw, current_layout.temp_unit);
    if (p->is_primary_pressure) return opendash_convert_pressure(raw, current_layout.pressure_unit);
    if (p->is_primary_speed)    return opendash_convert_speed(raw, current_layout.speed_unit);
    return raw;
}

static float convert_secondary(uint8_t page, float raw)
{
    if (page >= GAUGE_PAGE_COUNT) return raw;
    const gauge_page_t *p = &s_gauge_pages[page];
    if (p->is_secondary_temp)     return opendash_convert_temp(raw, current_layout.temp_unit);
    if (p->is_secondary_pressure) return opendash_convert_pressure(raw, current_layout.pressure_unit);
    if (p->is_secondary_speed)    return opendash_convert_speed(raw, current_layout.speed_unit);
    return raw;
}

/* ======================================================================
 * OUTLINED TEXT HELPERS
 *
 * LVGL doesn't have native text outline.  We create 4 shadow labels at
 * 1px offsets behind the main label to simulate an outline.
 * ====================================================================== */

static lv_obj_t* create_outlined_label(lv_obj_t *parent, const char *text,
                                        opendash_font_size_t font_size,
                                        uint32_t text_color, uint32_t outline_color,
                                        int8_t outline_px)
{
    lv_obj_t *container = lv_obj_create(parent);
    lv_obj_remove_style_all(container);
    lv_obj_set_size(container, LV_SIZE_CONTENT, LV_SIZE_CONTENT);
    lv_obj_set_style_pad_all(container, outline_px, 0);
    lv_obj_clear_flag(container, LV_OBJ_FLAG_SCROLLABLE);

    const int8_t offsets[4][2] = {
        {0, outline_px}, {outline_px * 2, outline_px},
        {outline_px, 0}, {outline_px, outline_px * 2}
    };
    for (int i = 0; i < 4; i++) {
        lv_obj_t *shadow = lv_label_create(container);
        lv_label_set_text(shadow, text);
        opendash_set_font(shadow, font_size);
        lv_obj_set_style_text_color(shadow, lv_color_hex(outline_color), 0);
        lv_obj_set_pos(shadow, offsets[i][0], offsets[i][1]);
    }

    lv_obj_t *main_label = lv_label_create(container);
    lv_label_set_text(main_label, text);
    opendash_set_font(main_label, font_size);
    lv_obj_set_style_text_color(main_label, lv_color_hex(text_color), 0);
    lv_obj_set_pos(main_label, outline_px, outline_px);

    return container;
}

/**
 * @brief Update all labels inside an outlined label container.
 *
 * Checks if text actually changed before calling lv_label_set_text
 * to avoid unnecessary LVGL invalidation (which can starve IDLE0 -> WDT).
 */
static void update_outlined_text(lv_obj_t *container, const char *new_text)
{
    if (container == NULL || new_text == NULL) return;
    uint32_t count = lv_obj_get_child_count(container);
    for (uint32_t i = 0; i < count; i++) {
        lv_obj_t *child = lv_obj_get_child(container, i);
        if (child && lv_obj_check_type(child, &lv_label_class)) {
            /* Only update if text actually changed — avoids invalidation storm */
            const char *cur = lv_label_get_text(child);
            if (cur == NULL || strcmp(cur, new_text) != 0) {
                lv_label_set_text(child, new_text);
            }
        }
    }
}

/* ======================================================================
 * SCREEN 1 — GAUGE MODE (shared by all gauge pages)
 *
 * Layout (see ASCII art at top of file):
 *   1  White outline arc — ARC_SIZE px, symmetric outline
 *   2  Colored indicator arc — ARC_SIZE px (same size, thinner width)
 *   3  Primary label ("OIL PRESS") — centered, y = PRIMARY_LABEL_Y
 *   4  Primary value ("47.2") — centered, y = PRIMARY_VALUE_Y
 *   5  Primary unit ("psi") — centered, y = PRIMARY_UNIT_Y
 *   6  Min/Max text ("MIN: 32  MAX: 68") — centered, y = PRIMARY_MINMAX_Y
 *   7  Secondary box — bordered rectangle, y = SECONDARY_BOX_Y
 *   8  Secondary label ("BOOST") — top-left of box
 *   9  Secondary value ("14.7") — centered in box
 *  10  Secondary unit ("kPa") — bottom-right of box
 * ====================================================================== */

static void create_gauge_screen(void)
{
    screen_gauge = lv_obj_create(NULL);
    lv_obj_set_style_bg_color(screen_gauge, lv_color_hex(0x000000), 0);

#if HAS_BACKGROUND_IMAGE
    lv_obj_t *bg = lv_image_create(screen_gauge);
    lv_image_set_src(bg, &background_right_dsc);
    lv_obj_set_size(bg, LCD_H_RES, LCD_V_RES);
    lv_obj_center(bg);
    lv_obj_set_style_image_opa(bg, 76, 0);
    lv_obj_move_to_index(bg, 0);
#endif

    /* -- 1) White outline arc (BEHIND colored arc) --
     *    LARGER bounding box than colored arc → extends outside.
     *    WIDER stroke than colored arc → extends inside.
     *    Net result: symmetric 5px border on both sides.
     */
    arc_outline = lv_arc_create(screen_gauge);
    lv_obj_set_size(arc_outline, ARC_OUTLINE_SIZE, ARC_OUTLINE_SIZE);
    lv_obj_align(arc_outline, LV_ALIGN_CENTER, 0, ARC_Y_OFFSET);
    lv_arc_set_rotation(arc_outline, ARC_ROTATION_DEG);
    lv_arc_set_bg_angles(arc_outline, 0, ARC_SWEEP_DEG);
    lv_arc_set_value(arc_outline, 0);
    lv_arc_set_range(arc_outline, 0, 100);
    lv_obj_remove_style(arc_outline, NULL, LV_PART_KNOB);
    lv_obj_remove_style(arc_outline, NULL, LV_PART_INDICATOR);
    lv_obj_clear_flag(arc_outline, LV_OBJ_FLAG_CLICKABLE);
    lv_obj_set_style_arc_width(arc_outline,
                               ARC_OUTLINE_WIDTH,
                               LV_PART_MAIN);
    lv_obj_set_style_arc_color(arc_outline, lv_color_hex(0xFFFFFF), LV_PART_MAIN);
    lv_obj_set_style_arc_rounded(arc_outline, true, LV_PART_MAIN);

    /* -- 2) Colored indicator arc (ON TOP of outline) --
     *    Smaller bounding box → inset from outline on outside.
     *    Thinner width → inset from outline on inside.
     */
    arc_gauge = lv_arc_create(screen_gauge);
    lv_obj_set_size(arc_gauge, ARC_SIZE, ARC_SIZE);
    lv_obj_align(arc_gauge, LV_ALIGN_CENTER, 0, ARC_Y_OFFSET);
    lv_arc_set_rotation(arc_gauge, ARC_ROTATION_DEG);
    lv_arc_set_bg_angles(arc_gauge, 0, ARC_SWEEP_DEG);
    lv_arc_set_value(arc_gauge, 0);
    lv_arc_set_range(arc_gauge, 0, 100);
    lv_obj_remove_style(arc_gauge, NULL, LV_PART_KNOB);
    lv_obj_clear_flag(arc_gauge, LV_OBJ_FLAG_CLICKABLE);
    /* Background track (unfilled) */
    lv_obj_set_style_arc_width(arc_gauge, ARC_WIDTH, LV_PART_MAIN);
    lv_obj_set_style_arc_color(arc_gauge, lv_color_hex(OPENDASH_COLOR_BG_SECTION), LV_PART_MAIN);
    lv_obj_set_style_arc_rounded(arc_gauge, true, LV_PART_MAIN);
    /* Indicator (filled sweep) */
    lv_obj_set_style_arc_width(arc_gauge, ARC_WIDTH, LV_PART_INDICATOR);
    lv_obj_set_style_arc_color(arc_gauge, lv_color_hex(0x00AAFF), LV_PART_INDICATOR);
    lv_obj_set_style_arc_rounded(arc_gauge, true, LV_PART_INDICATOR);

    /* -- 3) Primary label -- */
    primary_label = create_outlined_label(screen_gauge, "---",
                                           OPENDASH_FONT_SIZE_MEDIUM,
                                           OPENDASH_COLOR_TEXT_SECONDARY,
                                           OPENDASH_COLOR_TEXT_OUTLINE, 1);
    lv_obj_align(primary_label, LV_ALIGN_CENTER, 0, PRIMARY_LABEL_Y);

    /* -- 4) Primary value -- */
    primary_value = create_outlined_label(screen_gauge, "---",
                                           OPENDASH_FONT_SIZE_XXLARGE,
                                           OPENDASH_COLOR_TEXT_PRIMARY,
                                           OPENDASH_COLOR_TEXT_OUTLINE, 2);
    lv_obj_align(primary_value, LV_ALIGN_CENTER, 0, PRIMARY_VALUE_Y);

    /* -- 5) Primary unit suffix -- */
    primary_unit = create_outlined_label(screen_gauge, "",
                                          OPENDASH_FONT_SIZE_SMALL,
                                          OPENDASH_COLOR_TEXT_SECONDARY,
                                          OPENDASH_COLOR_TEXT_OUTLINE, 1);
    lv_obj_align(primary_unit, LV_ALIGN_CENTER, 0, PRIMARY_UNIT_Y);

    /* -- 6) Min/Max display -- */
    primary_minmax = create_outlined_label(screen_gauge, "",
                                            OPENDASH_FONT_SIZE_SMALL,
                                            OPENDASH_COLOR_TEXT_SECONDARY,
                                            OPENDASH_COLOR_TEXT_OUTLINE, 1);
    lv_obj_align(primary_minmax, LV_ALIGN_CENTER, 0, PRIMARY_MINMAX_Y);

    /* -- 7) Secondary box -- */
    secondary_box = lv_obj_create(screen_gauge);
    lv_obj_set_size(secondary_box, SECONDARY_BOX_W, SECONDARY_BOX_H);
    lv_obj_align(secondary_box, LV_ALIGN_CENTER, 0, SECONDARY_BOX_Y);
    lv_obj_set_style_bg_color(secondary_box, lv_color_hex(0x181818), 0);
    lv_obj_set_style_bg_opa(secondary_box, 242, 0);
    lv_obj_set_style_border_color(secondary_box, lv_color_hex(0x404040), 0);
    lv_obj_set_style_border_width(secondary_box, SECONDARY_BOX_BORDER, 0);
    lv_obj_set_style_radius(secondary_box, SECONDARY_BOX_RADIUS, 0);
    lv_obj_set_style_pad_all(secondary_box, 0, 0);
    lv_obj_clear_flag(secondary_box, LV_OBJ_FLAG_SCROLLABLE);

    /* -- 8) Secondary label -- */
    secondary_label = create_outlined_label(secondary_box, "---",
                                             OPENDASH_FONT_SIZE_MEDIUM,
                                             OPENDASH_COLOR_TEXT_SECONDARY,
                                             OPENDASH_COLOR_TEXT_OUTLINE, 1);
    lv_obj_align(secondary_label, LV_ALIGN_TOP_LEFT,
                 SECONDARY_LABEL_X, SECONDARY_LABEL_Y);

    /* -- 9) Secondary value -- */
    secondary_value = create_outlined_label(secondary_box, "---",
                                             OPENDASH_FONT_SIZE_XLARGE,
                                             OPENDASH_COLOR_TEXT_PRIMARY,
                                             OPENDASH_COLOR_TEXT_OUTLINE, 1);
    lv_obj_align(secondary_value, LV_ALIGN_CENTER, 0, SECONDARY_VALUE_Y);

    /* -- 10) Secondary unit -- */
    secondary_unit = create_outlined_label(secondary_box, "",
                                            OPENDASH_FONT_SIZE_MEDIUM,
                                            OPENDASH_COLOR_TEXT_SECONDARY,
                                            OPENDASH_COLOR_TEXT_OUTLINE, 1);
    lv_obj_align(secondary_unit, LV_ALIGN_BOTTOM_RIGHT,
                 SECONDARY_UNIT_X, SECONDARY_UNIT_Y);

    ESP_LOGI(TAG, "Gauge screen created (arc %dpx, outline %dpx, +/-%dpx symmetric)",
             ARC_SIZE, ARC_OUTLINE_SIZE, ARC_OUTLINE_EXTRA);
}

/**
 * @brief Apply a gauge page — swaps labels, unit suffixes, resets arc.
 *
 * Called on page switch or at init for page 0.
 * Does NOT create/destroy widgets, just updates text content.
 */
static void apply_gauge_page(uint8_t page)
{
    if (page >= GAUGE_PAGE_COUNT) return;
    const gauge_page_t *gp = &s_gauge_pages[page];

    /* Swap labels and unit suffixes */
    update_outlined_text(primary_label, gp->primary_label);
    update_outlined_text(primary_value, "---");
    update_outlined_text(primary_unit,  get_primary_suffix(page));

    update_outlined_text(secondary_label, gp->secondary_label);
    update_outlined_text(secondary_value, "---");
    update_outlined_text(secondary_unit,  get_secondary_suffix(page));

    /* Show min/max if we have data for this page */
    if (s_minmax[page].has_data) {
        char mm[48];
        float mn = convert_primary(page, s_minmax[page].primary_min);
        float mx = convert_primary(page, s_minmax[page].primary_max);
        snprintf(mm, sizeof(mm), "MIN:%.0f  MAX:%.0f", mn, mx);
        update_outlined_text(primary_minmax, mm);
    } else {
        update_outlined_text(primary_minmax, "");
    }

    /* Reset arc to 0 */
    lv_arc_set_value(arc_gauge, 0);

    /* Reset shift-light state */
    s_shift_active = false;
    s_shift_blink_on = false;
    /* Restore normal arc color */
    lv_obj_set_style_arc_color(arc_gauge, lv_color_hex(0x00AAFF), LV_PART_INDICATOR);

    ESP_LOGI(TAG, "Gauge page %d: %s + %s", page,
             gp->primary_label, gp->secondary_label);
}

