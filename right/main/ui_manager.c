/**
 * @file ui_manager.c
 * @brief OpenDash Left/Right/Pod Gauges — UI Manager Implementation
 *
 * Creates a professional gauge layout for the round 480x480 displays.
 * Shared by /left, /right, and future /pod# projects.
 *
 * ====================================================================
 *  MULTI-PAGE GAUGE SYSTEM
 * ====================================================================
 *
 *  The display cycles through N gauge pages + 1 odometer page.
 *  All gauge pages share a SINGLE set of LVGL widgets (screen_gauge).
 *  On page switch the labels, unit suffixes, and arc range are swapped
 *  instantly — no screen objects are created or destroyed.
 *
 *  Default pages (configurable via s_gauge_pages[]) — page 0 is boot default:
 *    Page 0: WHEEL SPEED MPH (arc) + GPS MPH (box)   [known-data default]
 *    Page 1: OIL PRESS (arc)       + BOOST (box)
 *    Page 2: WATER TEMP (arc)      + WHEEL MPH (box)
 *    Page 3: RPM (arc)             + AFR (box)       [shift-light enabled]
 *    Page 4: Odometer / Trip A
 *
 * ====================================================================
 *  GAUGE SCREEN LAYOUT  (480x480 Round Display)
 * ====================================================================
 *
 *               480 px
 *    +------------------------------+
 *   /                                \
 *  |  +-- Arc (270deg sweep) ------+  |
 *  |  | Outer: ARC_SIZE px         |  |   ARC_WIDTH = 45px
 *  |  | Width: 45px                |  |   ARC_OUTLINE_EXTRA = 5px
 *  |  | Outline: +5px symmetric    |  |   (extends 5px inside AND outside)
 *  |  |                            |  |
 *  |  |  +== Primary Value ======+ |  |   XXLARGE (96px) font
 *  |  |  |  "OIL PRESS"  (label) | |  |   Centered in display
 *  |  |  |    "47.2"      (value) | |  |
 *  |  |  |    "psi"       (unit)  | |  |
 *  |  |  |  MIN: 32  MAX: 68     | |  |   SMALL font, session high/low
 *  |  |  +========================+ |  |
 *  |  |                            |  |
 *  |  +--------+    +--------------+  |   90deg gap at bottom
 *  |       +-------------------+       |
 *  |       | BOOST      <-label|       |   +- SECONDARY BOX ----------+
 *  |       |     14.7   <-val  |       |   | 200x100 px, Y=+150      |
 *  |       |            kPa <-u|       |   | Label: TOP_LEFT  MED     |
 *  |       +-------------------+       |   | Value: CENTER    XLARGE  |
 *   \                                /    | Unit:  BOT_RIGHT MED     |
 *    +------------------------------+     +---------------------------+
 *
 * @see LVGL Documentation: https://docs.lvgl.io/master/
 * @see opendash_ui_styles.h for color definitions
 * @see opendash_fonts.h for font size definitions
 */

