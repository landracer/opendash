/* ======================================================================
 * WARNING OVERLAY — flashing red/orange fullscreen
 * ====================================================================== */

static void warning_flash_timer_cb(lv_timer_t *timer)
{
    (void)timer;
    if (warning_overlay == NULL) return;

    uint32_t elapsed = esp_log_timestamp() - warning_flash_start;
    if (warning_flash_duration > 0 && elapsed > warning_flash_duration) {
        ui_manager_warning_clear();
        return;
    }
    bool visible = ((elapsed / WARNING_FLASH_MS) % 2) == 0;
    if (visible) lv_obj_remove_flag(warning_overlay, LV_OBJ_FLAG_HIDDEN);
    else         lv_obj_add_flag(warning_overlay, LV_OBJ_FLAG_HIDDEN);
}

static void create_warning_overlay_on_screen(lv_obj_t *screen)
{
    if (warning_overlay != NULL) lv_obj_delete(warning_overlay);

    warning_overlay = lv_obj_create(screen);
    lv_obj_set_size(warning_overlay, LCD_H_RES, LCD_V_RES);
    lv_obj_center(warning_overlay);
    uint32_t color = (warning_level == OPENDASH_WARNING_CRITICAL)
                     ? OPENDASH_COLOR_WARNING_BOX_RED
                     : OPENDASH_COLOR_WARNING_BOX_ORANGE;
    lv_obj_set_style_bg_color(warning_overlay, lv_color_hex(color), 0);
    lv_obj_set_style_bg_opa(warning_overlay, 180, 0);
    lv_obj_set_style_border_width(warning_overlay, 0, 0);
    lv_obj_set_style_radius(warning_overlay, 0, 0);
    lv_obj_clear_flag(warning_overlay, LV_OBJ_FLAG_SCROLLABLE);
    lv_obj_clear_flag(warning_overlay, LV_OBJ_FLAG_CLICKABLE);
    lv_obj_move_foreground(warning_overlay);
}

/* ======================================================================
 * SHIFT-LIGHT — blinks arc color when RPM exceeds threshold
 *
 * Called from update_value when the current page has shift_light=true
 * and the arc percentage exceeds SHIFT_BLINK_THRESHOLD.
 * ====================================================================== */

static void shift_light_update(int arc_pct)
{
    if (current_page >= GAUGE_PAGE_COUNT) return;
    const gauge_page_t *gp = &s_gauge_pages[current_page];
    if (!gp->shift_light) return;

    if (arc_pct >= SHIFT_BLINK_THRESHOLD) {
        uint32_t now = esp_log_timestamp();
        if (!s_shift_active) {
            s_shift_active = true;
            s_shift_last_toggle_ms = now;
            s_shift_blink_on = true;
        }
        if (now - s_shift_last_toggle_ms >= SHIFT_BLINK_INTERVAL_MS) {
            s_shift_last_toggle_ms = now;
            s_shift_blink_on = !s_shift_blink_on;
        }
        /* Toggle between red and blue */
        uint32_t color = s_shift_blink_on ? 0xFF0000 : 0x00AAFF;
        lv_obj_set_style_arc_color(arc_gauge, lv_color_hex(color), LV_PART_INDICATOR);
    } else if (s_shift_active) {
        /* Dropped below threshold — restore normal color */
        s_shift_active = false;
        s_shift_blink_on = false;
        lv_obj_set_style_arc_color(arc_gauge, lv_color_hex(0x00AAFF), LV_PART_INDICATOR);
    }
}

/* ======================================================================
 * UI TASK — runs LVGL timer handler on core 1
 *
 * NOTE: uses display_lvgl_lock() to protect LVGL objects.  The main loop
 * on core 0 also locks before calling ui_manager_update_*.
 * ====================================================================== */

extern bool display_lvgl_lock(uint32_t timeout_ms);
extern void display_lvgl_unlock(void);

static void ui_task(void *pvParameters)
{
    ESP_LOGI(TAG, "UI task started on core 1");
    while (1) {
        if (display_lvgl_lock(50)) {
            lv_timer_handler();
            display_lvgl_unlock();
        }
        vTaskDelay(pdMS_TO_TICKS(20));
    }
}

/* ======================================================================
 * PUBLIC API — Initialization
 * ====================================================================== */

esp_err_t ui_manager_init(const opendash_display_layout_t *layout)
{
    if (layout == NULL) {
        ESP_LOGE(TAG, "Layout pointer is NULL");
        return ESP_ERR_INVALID_ARG;
    }
    memcpy(&current_layout, layout, sizeof(opendash_display_layout_t));

    /* Clear min/max tracking */
    memset(s_minmax, 0, sizeof(s_minmax));

    ESP_LOGI(TAG, "Creating multi-page UI for 480x480 round display");
    ESP_LOGI(TAG, "  %d gauge pages + 1 odometer = %d total screens",
             (int)GAUGE_PAGE_COUNT, (int)TOTAL_SCREEN_COUNT);
    ESP_LOGI(TAG, "  Units: temp=%s speed=%s pressure=%s distance=%s",
             opendash_get_temp_suffix(layout->temp_unit),
             opendash_get_speed_suffix(layout->speed_unit),
             opendash_get_pressure_suffix(layout->pressure_unit),
             opendash_get_distance_suffix(layout->distance_unit));

    create_gauge_screen();
    create_odo_screen();

    /* Start on page 0 (first gauge page) */
    current_page = 0;
    apply_gauge_page(0);
    lv_screen_load(screen_gauge);

    ESP_LOGI(TAG, "UI initialized — boot button cycles %d screens",
             (int)TOTAL_SCREEN_COUNT);
    return ESP_OK;
}

esp_err_t ui_manager_start(void)
{
    BaseType_t ret = xTaskCreatePinnedToCore(
        ui_task, "ui_task", 8192, NULL, 5, &ui_task_handle, 1);
    if (ret != pdPASS) {
        ESP_LOGE(TAG, "Failed to create UI task");
        return ESP_FAIL;
    }
    ESP_LOGI(TAG, "UI task started (core 1, 50Hz, LVGL-locked)");
    return ESP_OK;
}

void ui_manager_suspend(void)
{
    if (ui_task_handle) {
        vTaskSuspend(ui_task_handle);
        ESP_LOGW(TAG, "UI task suspended (BLE OTA)");
    }
}

/* ======================================================================
 * PUBLIC API — Data Updates (page-aware)
 *
 * Only updates widgets if the incoming data_point_id matches the
 * current gauge page's primary or secondary assignment.  This avoids
 * wasting CPU on updates the user can't see.
 * ====================================================================== */

void ui_manager_update_value(uint16_t data_point_id, float value)
{
    /* Skip if on odometer screen — gauge widgets aren't visible */
    if (current_page >= GAUGE_PAGE_COUNT) return;

    const gauge_page_t *gp = &s_gauge_pages[current_page];
    char buf[32];

    /* --- Primary (arc) value --- */
    if (data_point_id == gp->primary_dp) {
        float display_val = convert_primary(current_page, value);

        /* Format: temperature = no decimals, pressure = 1 decimal, RPM = no decimals */
        if (gp->is_primary_temp || gp->primary_dp == OPENDASH_DP_RPM) {
            snprintf(buf, sizeof(buf), "%.0f", display_val);
        } else {
            snprintf(buf, sizeof(buf), "%.1f", display_val);
        }
        update_outlined_text(primary_value, buf);

        /* Update arc percentage: map raw value to 0-100% */
        float range = gp->arc_max - gp->arc_min;
        int pct = (range > 0.0f) ? (int)(((value - gp->arc_min) / range) * 100.0f) : 0;
        if (pct < 0) pct = 0;
        if (pct > 100) pct = 100;
        lv_arc_set_value(arc_gauge, pct);

        /* Track session min/max (in raw/internal units) */
        page_minmax_t *mm = &s_minmax[current_page];
        if (!mm->has_data) {
            mm->primary_min = value;
            mm->primary_max = value;
            mm->has_data = true;
        } else {
            if (value < mm->primary_min) mm->primary_min = value;
            if (value > mm->primary_max) mm->primary_max = value;
        }
        /* Update min/max display */
        float mn = convert_primary(current_page, mm->primary_min);
        float mx = convert_primary(current_page, mm->primary_max);
        snprintf(buf, sizeof(buf), "MIN:%.0f  MAX:%.0f", mn, mx);
        update_outlined_text(primary_minmax, buf);

        /* Shift-light check */
        shift_light_update(pct);
        return;
    }

    /* --- Secondary (box) value --- */
    if (data_point_id == gp->secondary_dp) {
        float display_val = convert_secondary(current_page, value);
        if (gp->is_secondary_speed || gp->secondary_dp == OPENDASH_DP_RPM) {
            snprintf(buf, sizeof(buf), "%.0f", display_val);
        } else if (gp->secondary_dp == OPENDASH_DP_AFR) {
            snprintf(buf, sizeof(buf), "%.1f", display_val);
        } else {
            snprintf(buf, sizeof(buf), "%.1f", display_val);
        }
        update_outlined_text(secondary_value, buf);
        return;
    }

    /* Data point not on current page — silently ignore */
}

/* ======================================================================
 * PUBLIC API — Odometer Update
 *
 * Only updates LVGL labels when the odometer screen is actually visible.
 * Adds taskYIELD() between the two outlined-text updates to let the
 * IDLE task run briefly and prevent WDT starvation on CPU 0.
 * ====================================================================== */

void ui_manager_update_odometer(const opendash_odometer_t *odo)
{
    if (odo == NULL) return;

    /* Guard: only update when ODO screen is visible.
     * Writing to invisible labels still triggers lv_obj_invalidate
     * which monopolizes CPU 0 -> starves IDLE0 -> WDT crash.        */
    if (current_page != ODO_SCREEN_INDEX) return;

    char buf[32];
    const char *suffix = opendash_get_distance_suffix(current_layout.distance_unit);

    /* Odometer total */
    float total = opendash_convert_distance(
        opendash_odometer_get_km(odo), current_layout.distance_unit);
    snprintf(buf, sizeof(buf), "%.1f %s", total, suffix);
    update_outlined_text(odo_value, buf);

    /* Yield between the two heavy outlined-text updates to let IDLE0 run.
     * Each update touches 5 labels (4 shadow + 1 main) and triggers
     * LVGL invalidation; doing both back-to-back can starve IDLE for
     * >5 seconds on a loaded system -> WDT timeout.                     */
    taskYIELD();

    /* Trip A */
    float trip = opendash_convert_distance(
        opendash_odometer_get_trip_a_km(odo), current_layout.distance_unit);
    snprintf(buf, sizeof(buf), "%.1f %s", trip, suffix);
    update_outlined_text(trip_value, buf);
}

/* ======================================================================
 * PUBLIC API — Warning System
 * ====================================================================== */

esp_err_t ui_manager_warning_trigger(opendash_warning_level_t level,
                                      const char *message,
                                      uint32_t flash_ms)
{
    (void)message;
    warning_level = level;
    warning_flash_duration = flash_ms;
    warning_flash_start = esp_log_timestamp();

    lv_obj_t *active = (current_page < GAUGE_PAGE_COUNT) ? screen_gauge : screen_odo;
    create_warning_overlay_on_screen(active);

    if (warning_timer == NULL) {
        warning_timer = lv_timer_create(warning_flash_timer_cb, WARNING_FLASH_MS, NULL);
    }
    ESP_LOGI(TAG, "Warning triggered: level=%d, duration=%lu ms",
             level, (unsigned long)flash_ms);
    return ESP_OK;
}

esp_err_t ui_manager_warning_clear(void)
{
    if (warning_timer != NULL) {
        lv_timer_delete(warning_timer);
        warning_timer = NULL;
    }
    if (warning_overlay != NULL) {
        lv_obj_delete(warning_overlay);
        warning_overlay = NULL;
    }
    warning_level = OPENDASH_WARNING_NONE;
    ESP_LOGI(TAG, "Warning cleared");
    return ESP_OK;
}

/* ======================================================================
 * PUBLIC API — Screen Navigation
 *
 * Screens cycle: Gauge[0] -> Gauge[1] -> ... -> Gauge[N-1] -> ODO -> Gauge[0]
 * ====================================================================== */

esp_err_t ui_manager_next_screen(void)
{
    uint8_t next = (current_page + 1) % TOTAL_SCREEN_COUNT;

    /* Clear any active warning */
    ui_manager_warning_clear();

    current_page = next;

    if (next < GAUGE_PAGE_COUNT) {
        apply_gauge_page(next);
        lv_screen_load(screen_gauge);
        ESP_LOGI(TAG, "Switched to GAUGE page %d/%d (%s + %s)",
                 next, (int)GAUGE_PAGE_COUNT,
                 s_gauge_pages[next].primary_label,
                 s_gauge_pages[next].secondary_label);
    } else {
        lv_screen_load(screen_odo);
        ESP_LOGI(TAG, "Switched to ODOMETER screen");
    }

    return ESP_OK;
}

uint8_t ui_manager_get_current_screen(void)
{
    return current_page;
}

uint8_t ui_manager_get_screen_count(void)
{
    return (uint8_t)TOTAL_SCREEN_COUNT;
}
