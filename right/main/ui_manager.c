/* --- Arc gauge dimensions ---
 *     TWO concentric arcs are drawn:
 *       1) White outline arc — LARGER bounding box, wider stroke
 *       2) Colored indicator — smaller bounding box, normal stroke
 *
 *     LVGL draws arcs INWARD from the bounding-box outer edge.
 *     To get a symmetric outline (N px on EACH side of the colored arc):
 *       - Outline size  = ARC_SIZE + 2*OUTLINE_EXTRA  (extends outside)
 *       - Outline width = ARC_WIDTH + 2*OUTLINE_EXTRA (extends inside)
 *
 *     Visual proof (radii, center of display = 0):
 *       Outline outer = (ARC_SIZE+2*5)/2 = 230   Colored outer = 450/2 = 225
 *       Outline inner = 230 - 55 = 175            Colored inner = 225 - 45 = 180
 *       Outside gap = 230 - 225 = 5px  ✓          Inside gap = 180 - 175 = 5px  ✓
 */
